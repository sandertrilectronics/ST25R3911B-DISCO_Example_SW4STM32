int main(void)
{

  /* USER CODE BEGIN 1 */
  bootloaderCheck4Enter();

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_LPUART1_UART_Init();
  MX_USART1_UART_Init();
  MX_SPI1_Init();
  MX_USB_DEVICE_Init();

  /* USER CODE BEGIN 2 */
  
  /* SPI Connection to ST25R3911 */
  spiInit(&hspi1);
  
  /* Use LPUART1 as logging UART */
  logUsartInit(&hlpuart1);
  logUsart("Welcome to DISCO-STM32L4x6\n");
  
  /* Initalize RFAL */
  rfalAnalogConfigInitialize();
  if( rfalInitialize() == ERR_NONE )
  {
      logUsart("RFAL initialization succeeded..\n");
      platformLedOn(PLATFORM_LED_FIELD_PORT, PLATFORM_LED_FIELD_PIN);
      ledOn(LED_A);
      ledOn(LED_B);
      ledOn(LED_F);
      ledOn(LED_V);
      ledOn(LED_AP2P);
  }
  else
  {
      while(1){
        platformLedToogle(PLATFORM_LED_FIELD_PORT, PLATFORM_LED_FIELD_PIN);
        ledToggle(LED_A);
        ledToggle(LED_B);
        ledToggle(LED_F);
        ledToggle(LED_V);
        ledToggle(LED_AP2P);
        HAL_Delay(40);
        logUsart("RFAL initialization failed..\n");
      }
  }

  /* For production programming: Turn ON field led, diabled by 
   * rfalSetAnalogConfig when accessing op control */
  platformLedOn(PLATFORM_LED_FIELD_PORT, PLATFORM_LED_FIELD_PIN);

  /* Initalize SW components */
  StreamDispatcherInit();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
      /*  */
      ProcessIO();
      dispatcherInterruptHandler();
      rfalWorker();
      
  }
  /* USER CODE END 3 */

}

